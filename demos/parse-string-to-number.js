"use strict";

//parseInt
//declaring variable a and assigning a string to it
var a = " 100 " ;
var b = parseInt(a);
console.log("b is " + b);

var dataTypeOfAVariable = typeof a;
console.log(dataTypeOfAVariable);

var dataTypeOfBVariable = typeof b;
console.log(dataTypeOfBVariable);


//parseFloat
var c = "100.13" ;
var d = parseFloat(c);
console.log("d is " + d);

var dataTypeOfCVariable = typeof c;
console.log(dataTypeOfCVariable);

var dataTypeOfDVariable = typeof d;
console.log(dataTypeOfDVariable);